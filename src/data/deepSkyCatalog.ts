export type DeepSkyType = "emission" | "planetary" | "reflection" | "supernova_remnant";

export interface DeepSkyObject {
  id: string;
  name: string;
  raHours: number;
  decDeg: number;
  /** Apparent width on the sky, arcminutes */
  widthArcmin: number;
  heightArcmin: number;
  type: DeepSkyType;
  peakOpacity: number;
  /** Optional multiply tint on the sprite material (hex, e.g. 0xff8866) */
  tint?: number;
  description: string;
  age: string;
}

function ra(h: number, m: number, s: number): number {
  return h + m / 60 + s / 3600;
}

function dec(deg: number, m: number, s: number, sign: 1 | -1 = 1): number {
  return sign * (Math.abs(deg) + m / 60 + s / 3600);
}

